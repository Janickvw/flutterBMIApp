        children: <Widget>[
          Expanded(
            child: Row(
              children: <Widget>[
                Expanded(
                  child: GestureDetector(
                    onTap: () {
                      setState(() {
                        maleCardColour = activeCardColour;
                      });
                    },
                    child: ReusableCard(
                      cardColour: maleCardColour,
                      cardChild: IconContent(
                        inputIcon: FontAwesomeIcons.mars,
                        inputString: 'MALE',
                      ),
                    ),
                  ),
                ),
                Expanded(
                  child: GestureDetector(
                    onTap: () {
                      setState(() {});
                    },
                    child: ReusableCard(
                      cardColour: femaleCardColour,
                      cardChild: IconContent(
                          inputIcon: FontAwesomeIcons.venus,
                          inputString: 'FEMALE'),
                    ),
                  ),
                ),
              ],
            ),
          ),
          Expanded(
            child: Row(
              children: <Widget>[
                Expanded(
                  child: ReusableCard(
                    cardColour: activeCardColour,
                  ),
                ),
              ],
            ),
          ),
